//boolean type
let isTrue = true;
let isFalse = false;

console.log(isTrue);
console.log(isFalse);

console.clear();

//!! expression (change value into boolean)
//Falshy
console.log(!!0);
console.log(!!-0);
console.log(!!'');
console.log(!!null);
console.log(!!undefined);
console.log(!!NaN);

//Truthy
console.log(!!1);
console.log(!!-1);
console.log(!!'text');
console.log(!!{});
console.log(!!Infinity);
